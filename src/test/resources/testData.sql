CREATE TABLE if not exists product_type
(
    product_type_id bigint NOT NULL,
    value           character varying(255),
    sort_value bigint,
    CONSTRAINT product_type_pkey PRIMARY KEY (product_type_id)
);



CREATE TABLE if not exists product_categories
(
    product_category_id bigint NOT NULL,
    name_of_category    character varying(255),
    product_type_id     bigint,
    sort_value bigint,
    CONSTRAINT product_categories_pkey PRIMARY KEY (product_category_id),
    CONSTRAINT product_categories_name_of_category_key UNIQUE (name_of_category),
    CONSTRAINT fk_product_categories_product_type FOREIGN KEY (product_type_id)
        REFERENCES product_type (product_type_id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
);



CREATE TABLE if not exists product_spec_names
(
    product_spec_name_id bigint NOT NULL,
    spec_name            character varying(255),
    product_category_id  bigint,
    sort_value bigint,
    CONSTRAINT product_spec_names_pkey PRIMARY KEY (product_spec_name_id),
    CONSTRAINT product_spec_names_spec_name_key UNIQUE (spec_name),
    CONSTRAINT fk_product_spec_names_product_categories FOREIGN KEY (product_category_id)
        REFERENCES product_categories (product_category_id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
);



CREATE TABLE if not exists product_spec_value
(
    product_spec_value_id bigint NOT NULL,
    value                 character varying(255),
    product_spec_name_id  bigint,
    CONSTRAINT product_spec_value_pkey PRIMARY KEY (product_spec_value_id),
    CONSTRAINT fk_product_spec_value_product_spec_names FOREIGN KEY (product_spec_name_id)
        REFERENCES product_spec_names (product_spec_name_id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
);



CREATE TABLE if not exists products
(
    product_id          bigint NOT NULL,
    name                character varying(255),
    price               double precision,
    description         varchar(255),
    quantity            integer,
    product_category_id bigint,
    rating              double precision,
    shadowRating        double precision,
    CONSTRAINT products_pkey PRIMARY KEY (product_id),
    CONSTRAINT fk_products_product_categories FOREIGN KEY (product_category_id)
        REFERENCES product_categories (product_category_id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
);



